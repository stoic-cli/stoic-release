// ArmoredPrivateKey will output a private key in ascii armored form
// useful for debugging in some circumstances.
func ArmoredPrivateKey(pk *memguard.LockedBuffer) (string, error) {
	entity, err := openpgp.ReadEntity(packet.NewReader(bytes.NewReader(pk.Buffer())))
	if err != nil {
		return "", err
	}
	var privKey, privKeyArmored bytes.Buffer
	err = entity.SerializePrivate(&privKey, nil)
	if err != nil {
		return "", err
	}
	w, err := armor.Encode(&privKeyArmored, openpgp.PrivateKeyType, map[string]string{})
	if err != nil {
		return "", err
	}
	_, err = w.Write(privKey.Bytes())
	if err != nil {
		return "", err
	}
	err = w.Close()
	if err != nil {
		return "", err
	}
	return privKeyArmored.String(), nil
}

// ArmoredToByte will take an armored key and
// return its byte form
func ArmoredToByte(in []byte) ([]byte, error) {
	out, err := armor.Decode(bytes.NewReader(in))
	if err != nil {
		return nil, err
	}
	var buf bytes.Buffer
	_, err = io.Copy(&buf, out.Body)
	if err != nil {
		return nil, err
	}
	return buf.Bytes(), nil
}

